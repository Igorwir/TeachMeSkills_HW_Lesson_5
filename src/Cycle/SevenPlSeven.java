package Cycle;
/*
We need displaying numbers with cycle while
 7 14 21 28 35 42 49 56 63 70 77 84 91 98 */
public class SevenPlSeven {
    public static void main(String[] args) {
        int num = 7;
        while (num <100){
            System.out.print(num + " ");
            num = num + 7;

        }

    }
}
