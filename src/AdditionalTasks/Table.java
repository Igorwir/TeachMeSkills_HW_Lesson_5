package AdditionalTasks;

public class Table {
    public static void main(String[] args) {
        for (int i = 0; i <= 9; i++) {
            for (int j = 0; j <=9; j++) {
                System.out.print(i + " * "+ j + " = " + i * j + "\t");
            }
            System.out.println();
        }
    }
}
